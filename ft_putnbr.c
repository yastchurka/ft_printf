#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	sign;

	sign = 0;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return (11);
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
		sign++;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) + '0');
	}
	else
		ft_putchar(n + '0');
	return (ft_leng(n) + sign);
}
